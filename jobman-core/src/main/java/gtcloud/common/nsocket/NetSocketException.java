package gtcloud.common.nsocket;

@SuppressWarnings("serial")
public class NetSocketException extends Exception {

    public NetSocketException() {
    }

    public NetSocketException(String arg0) {
        super(arg0);
    }

    public NetSocketException(Throwable arg0) {
        super(arg0);
    }

    public NetSocketException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }
}
